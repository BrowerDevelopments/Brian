package com.example.brower.brian;

public class SourceClass {

    String lobe;
    String name;
    String link;

    public SourceClass(String lobe, String name, String link) {

        this.lobe = lobe;
        this.name = name;
        this.link = link;

    }

    public String getLobe() {
        return lobe;
    }

    public String getName() {
        return name;
    }

    public String getLink() {
        return link;
    }
}
